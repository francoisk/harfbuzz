#include <harfbuzz/hb.h>

#ifdef TEST_ICU
#include <harfbuzz/hb-icu.h>
#endif

using namespace std;

// This array contains /usr/share/fonts/X11/misc/courO08.pcf.gz from my dev
// machine.
//
extern const unsigned char font_file[12369];

int
main ()
{
  // This is a slightly-modified version of the code at
  // https://harfbuzz.github.io/a-simple-shaping-example.html.
  //

  // Create a buffer and put your text in it.
  //
  hb_buffer_t* buf;
  buf = hb_buffer_create ();
  hb_buffer_add_utf8 (buf, "foo", -1, 0, -1);

  // Set the script, language and direction of the buffer.
  //
  hb_buffer_set_direction (buf, HB_DIRECTION_LTR);
  hb_buffer_set_script (buf, HB_SCRIPT_LATIN);
  hb_buffer_set_language (buf, hb_language_from_string ("en", -1));

#ifdef TEST_ICU
  // Attach the ICU unicode functions to the buffer (as per
  // https://harfbuzz.github.io/integration-icu.html).
  //
  hb_unicode_funcs_t* icufunctions;
  icufunctions = hb_icu_get_unicode_funcs ();
  hb_buffer_set_unicode_funcs (buf, icufunctions);
#endif

  // Create a face and a font from a font file.
  //
  hb_blob_t* blob = hb_blob_create (reinterpret_cast<const char*> (font_file),
                                    sizeof (font_file),
                                    HB_MEMORY_MODE_READONLY,
                                    nullptr,
                                    nullptr);
  if (blob == hb_blob_get_empty ())
    return 1;
  hb_face_t* face = hb_face_create (blob, 0);
  hb_font_t* font = hb_font_create (face);

  // Shape!
  //
  hb_shape (font, buf, nullptr, 0);

  // Get the glyph and position information.
  //
  unsigned int glyph_count;
  static_cast<void> (hb_buffer_get_glyph_infos (buf, &glyph_count));
  hb_glyph_position_t* glyph_pos =
      hb_buffer_get_glyph_positions (buf, &glyph_count);

  // Iterate over each glyph.
  //
  hb_position_t cursor_x = 0;
  hb_position_t cursor_y = 0;
  for (unsigned int i = 0; i < glyph_count; i++)
  {
    hb_position_t x_advance = glyph_pos[i].x_advance;
    hb_position_t y_advance = glyph_pos[i].y_advance;
    cursor_x += x_advance;
    cursor_y += y_advance;
  }

  // Tidy up.
  //
  hb_buffer_destroy (buf);
  hb_font_destroy (font);
  hb_face_destroy (face);
  hb_blob_destroy (blob);

  return 0;
}

